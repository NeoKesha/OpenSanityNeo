	XOR EAX, EAX
	FSTSW AX
	AND AX, 3Fh
	MOV dword ptr [?debugFPUStatus@@3HA], EAX
	CALL ?CheckFPUState@@YGXXZ
	RET
DBG_CHECK_FPU_STATE ENDP

