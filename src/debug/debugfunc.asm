DBG_PRINT_FPU_STATE PROC
	XOR EAX, EAX
	FSTSW AX
	AND AX, 3Fh
	MOV dword ptr [?debugPrintValue@@3HA], EAX
	MOV EAX, OFFSET DBG_FSTATUS
	MOV [?debugPrintString@@3PADA], EAX
	CALL ?DbgPrintValue@@YGXXZ
	RET
DBG_PRINT_FPU_STATE ENDP

