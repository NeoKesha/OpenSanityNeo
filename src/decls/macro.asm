;;Common macro

TwinMain MACRO procName, conv
    main PROC c
	IFDEF DEBUG_ENABLED
		DEBUG_PROLOGUE_MACRO
		CALL DBG_INIT
		DEBUG_EPILOGUE_MACRO
	ENDIF
	TwinCall FUN_000C62A4
ENDM

TwinProcThunk MACRO procName, conv
    procName PROC conv
ENDM

TwinProcThiscall MACRO procName, conv
	procName PROC stdcall
	IFDEF DEBUG_ENABLED
		DEBUG_PROLOGUE_MACRO
		PUSH EAX
		MOV EAX, OFFSET @CatStr(<DBG_>, procName)
		MOV [?debugCurrentFunction@@3PADA], EAX
		POP EAX
		DEBUG_HEADER_MACRO
		DEBUG_EPILOGUE_MACRO
	ENDIF
ENDM

TwinProc MACRO procName, conv
    procName PROC conv
	IFDEF DEBUG_ENABLED
		DEBUG_PROLOGUE_MACRO
		PUSH EAX
		MOV EAX, OFFSET @CatStr(<DBG_>, procName)
		MOV [?debugCurrentFunction@@3PADA], EAX
		POP EAX
		DEBUG_HEADER_MACRO
		DEBUG_EPILOGUE_MACRO
	ENDIF
ENDM

TwinProcEnd MACRO procName
    procName ENDP
ENDM

TwinCall MACRO procName
	CALL procName
ENDM

TwinProcExit MACRO arg1
    RET arg1  
ENDM

